class Typecasting
  {
	public static void main(String args[])
	{
	int i = ( int )20.5f;	
	System.out.println("The value after explicit casting is"+i);
	float f=i;
	System.out.println("The value after implicit casting is"+f);
	long l=1333746943;
	double d=l;
	System.out.println("The value after casting is"+d);
	

}
}